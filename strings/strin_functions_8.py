a = input('enter something')

if a.isupper():
    print('a is upper')

if a.islower():
    print('a is lower')

if a.istitle():
    print('a is title case')

if a.isalpha():
    print('a only contain alphabets')

if a.isalnum():
    print('a contains alphabets and numbers only')

if a.isnumeric():
    print('a contains numbers only')

if a.isspace():
    print('a only contains spaces')

if a.isprintable():
    print('a can be printed')

if a.isascii():
    print('is ascii character')

if a.isdigit():
    print('is contains digits only')

if a.isdecimal():
    print(' is contans decimal only')

   



















   